export function createTranslator(locale) {
  return (key) => translate(locale, key);
}

export function translate(locale, key) {
  return valueAtPath(MESSAGES[locale], key) || valueAtPath(MESSAGES.ko, key) || key;
}

export function translateApiError(locale, error, fallbackKey) {
  const codeKey = errorCodeKey(error?.code);
  if (codeKey) {
    return translate(locale, codeKey);
  }
  return error?.message || translate(locale, fallbackKey);
}

function errorCodeKey(code) {
  const keys = {
    EWGF_REQUEST_FAILED: 'errors.ewgfRequestFailed',
    EWGF_EMPTY_RESPONSE: 'errors.ewgfEmptyResponse',
    EWGF_INVALID_JSON: 'errors.ewgfInvalidJson',
    PLAYER_API_ERROR: 'errors.playerApiError',
  };
  return keys[code] || null;
}

function valueAtPath(source, key) {
  return key.split('.').reduce((current, part) => current?.[part], source);
}
